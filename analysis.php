					<p>This graph illustrates the number of journeys taken on the bike scheme per quarter (Q1: Jan-Mar; Q2: Apr-Jun; Q3: Jul-Sep; Q4: Oct-Dec) since its introduction. The total number of journeys is divided by gender - male, female, and those that did not identify.

These data demonstrate that the majority of users are male; they account for ~75% of all journeys made. This suggests that Citi bike marketing may wish to focus advertising and incentives on women to join the scheme, to maximize the program's growth and revenue.

These data also illustrate the cyclical nature of use, with more journeys in the summer (Q3) compared to the winter (Q1). This suggests that the scheme may want to tailor bike availbility for the season. For example, have less bikes in operation during the winter.</p>

