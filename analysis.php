					<p>Data for journeys made on the scheme can be accessed from <a href="https://s3.amazonaws.com/tripdata/index.html">Amazon web services</a>.
									Data is available for each month from June 2013 - the scheme's first operational month. Each datafile contains information on: trip duration (including timestamps for trip start and end); start/end station (including station ID); bike ID; user type (i.e., annual subscriber or customer); gender; and year of birth. Learn more about Citi Bike by visiting its <a href="https://www.citibikenyc.com/">website</a>.</p>
									<p>Quarters in the above analyses are defined as: Q1 - January to March; Q2 - April to June; Q3 - July to September; Q4 - October to December.</p>
